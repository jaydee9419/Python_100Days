# list

a = ["Apple", "Mango", "Cherry","Banana"]

#print list
print(a)

#negative list
print(a[::-1])

#print between range based on index value and in below example 3 is excludeed
print(a[0:3])

#print from last and last item value will be -1
print(a[:-2])

# add item to list
a.append("Orange")
print(a)

# remove item from list
a.remove("Mango")
print(a)